    private boolean salvar() {
       PreparedStatement ps;
    String nome3 = txtNome3.getText();
    String senha2 = txtSenha2.getText();
    String horario = txtHorario.getText();
    String especialista = txtEspecialista.getText();
    String sql = "INSERT INTO Consulta (nome, senha, especialista, horario) VALUES (?, ?, ?, ?)";
    
    Connection conn = Conexao.getConexao();
    
        try{
            ps = conn.prepareStatement(sql);
            ps.setString(1, nome3);
            ps.setString(2, senha2);
            ps.setString(3, especialista);
            ps.setString(4, horario);
            
            ps.executeUpdate();
        } catch (SQLException ex) {
            Logger.getLogger(Consulta.class.getName()).log(Level.SEVERE, null, ex);
        }finally{
            try{
                conn.close();
            }catch(SQLException e){
                e.printStackTrace();
            }
        }
    return false;
}
