private boolean salvar(){
    PreparedStatement ps;
    String nome = txtNome.getText();
    String tel = txtTelefone.getText();
    String idade = txtIdade.getText();
    String ender = txtEndereco.getText();
    String comple = txtComplemento.getText();
    String bairro = txtBairro.getText();
    String cep = txtCep.getText();
    String uf = txtUF.getText();
    
    String sql = "INSERT INTO CadastroPac (nome, telefone, idade, endereco, complemento, bairro, cep, UF) VALUES (?, ?, ?, ?, ?, ?, ?, ?)";
    
    Connection conn = Conexao.getConexao();
    
        try{
            ps = conn.prepareStatement(sql);
            ps.setString(1, nome);
            ps.setString(2, tel);
            ps.setString(3, idade);
            ps.setString(4, ender);
            ps.setString(5, comple);
            ps.setString(6, bairro);
            ps.setString(7, cep);
            ps.setString(8, uf);
            
            ps.executeUpdate();
        } catch (SQLException ex) {
            Logger.getLogger(CadastroUsuario.class.getName()).log(Level.SEVERE, null, ex);
        }finally{
            try{
                conn.close();
            }catch(SQLException e){
                e.printStackTrace();
            }
        }
    return false;
}

